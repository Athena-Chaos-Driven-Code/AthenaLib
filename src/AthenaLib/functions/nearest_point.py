POINT = tuple[NUMBER, NUMBER] | list[NUMBER, NUMBER]

# ----------------------------------------------------------------------------------------------------------------------
# - Code -
# ----------------------------------------------------------------------------------------------------------------------
def closest_point_index(point:POINT, points:list[POINT]) -> np.ndarray[int]:
    nodes = np.asarray(points)
    dist_2 = np.sum((nodes - point)**2, axis=1)
    return np.argmin(dist_2)